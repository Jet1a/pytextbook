# Tuples
tup = (1,2,3,4,"stringok",False,3.14) #tuple ()
lis = [1,2,3,4,"stringok",False,3.14] #list []

## change tuple to list
lis = list(tup)
lis[0] = "Yippee"
print("Before :",tup)
tup = tuple(lis)
print("After :",tup)
## if loop
if "stringok" in tup:
    print("Ya it is")
else :
    print("Nah")


